document.addEventListener('DOMContentLoaded', () => {
  document.querySelector('#imara-story .story-box').innerHTML = imaraStory;
  document.querySelector('#davan-story .story-box').innerHTML = davanStory;
});

const visited = new Set();

function goTo(pageId) {
  document.querySelector('.page.active').classList.remove('active');
  document.getElementById(pageId).classList.add('active');

  if (pageId === 'imara-story' || pageId === 'davan-story') {
    visited.add(pageId);
    updateStoryButton(pageId);
  }
}

function updateStoryButton(pageId) {
  const other = pageId === 'imara-story' ? 'davan-story' : 'imara-story';
  const otherLabel = pageId === 'imara-story' ? 'DAVAN' : 'IMARA';
  const btn = document.querySelector(`#${pageId} button`);

  if (visited.has(other)) {
    btn.textContent = 'HOME';
    btn.onclick = () => goTo('home');
  } else {
    btn.textContent = otherLabel;
    btn.onclick = () => goTo(other);
  }
}
